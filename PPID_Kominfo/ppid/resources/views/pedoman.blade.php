                    11. Pejabat Pengelola Informasi dan Dokumentasi yang selanjutnya disebut PPID adalah pejabat struktural yang bertanggung jawab dalam pengumpulan, pendokumentasian, penyimpanan, pemeliharaan, penyediaan, distribusi dan pelayanan informasi di Pemerintahan Daerah;<br>
                    12. PPID Pembantu adalah pejabat struktural yang bertanggung jawab dalam pengumpulan, pendokumentasian, penyimpanan, pemeliharaan, penyediaan, distribusi dan pelayanan informasi di lingkungan SKPD;<br>
                    13. Atasan PPID adalah pejabat yang merupakan atasan langsung PPID;<br>
                    14. Daftar Informasi Publik adalah catatan yang berisi keterangan secara sistematis tentang seluruh Informasi Publik yang berada dibawah penguasaan Pemerintah Daerah tidak termasuk informasi yang dikecualikan;<br>
                    15. Pengguna Informasi Publik adalah orang yang menggunakan informasi publik sebagaimana diatur dalam ketentuan peraturan perundang-undangan;<br>
                    16. Orang adalah orang perseorangan, kelompok orang, badan hukum, atau badan publik;<br>
                    17. Pemohon Informasi Publik adalah warga negara dan/atau badan hukum Indonesia yang mengajukan permintaan informasi publik sebagaimana diatur dalam ketentuan peraturan perundang-undangan.<br>

